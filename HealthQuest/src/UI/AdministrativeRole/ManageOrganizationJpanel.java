    private void populateComboBox(){
        organizationComboBox.removeAllItems();
        for (Organization.Type type : Organization.Type.values()){
            
            if (!type.getValue().equals(Organization.Type.Admin.getValue()))
            {
//                
                if(enterprise instanceof HospitalEnterprise){
                    
                     organizationComboBox.addItem(Organization.Type.Doctor);
                     organizationComboBox.addItem(Organization.Type.Lab);
                     break;
                }else if(enterprise instanceof MedicamentEnterprise){
                    
                     organizationComboBox.addItem(Organization.Type.medicament);
                     break;
                    
                }
                else if(enterprise instanceof BioTechnologyEnterprise)
                {
                     organizationComboBox.addItem(Organization.Type.BioTechnology);
                     break;
                }
                else if(enterprise instanceof ChemicalEnterprise)
                {
                  organizationComboBox.addItem(Organization.Type.Chemical);
                     break;  
                }
                 
                    
            }
        }
    }
     private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) organizationtbl.getModel();
        
        model.setRowCount(0);
        
        for (Organization organization : organizationDir.getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();
            
            model.addRow(row);
        }
    }

