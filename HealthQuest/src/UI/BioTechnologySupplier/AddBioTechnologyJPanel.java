    private void addBioTechnologyBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBioTechnologyBtnActionPerformed
        // TODO add your handling code here:

        String BioTechnologyName = BioTechnologytxtField.getText().trim();
        if(BioTechnologyName.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"BioTechnology name cannot be empty");
            return;
        }
        ArrayList<String> BioTechnologycheck= new ArrayList<>();
        for(BioTechnology d:business.getBioTechnologyList().getBioTechnologyList())
        {
            BioTechnologycheck.add(d.getBioTechnologyName().toLowerCase());
        }
        if(BioTechnologycheck.contains(BioTechnologyName.toLowerCase()))
        {
            JOptionPane.showMessageDialog(null,"BioTechnology name already exists");
            return;
        }

        business.getBioTechnologyList().addBioTechnologyList().setBioTechnologyName(BioTechnologyName);
        
        populateTable();
        log.debug(userAccount+" "+"added new BioTechnology"+" "+BioTechnologyName);
        BioTechnologytxtField.setText("");

    }//GEN-LAST:event_addBioTechnologyBtnActionPerformed

    private void addChemicalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addChemicalButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = BioTechnologyTble.getSelectedRow();
        if(selectedRow<0)
        {
            JOptionPane.showMessageDialog(null, "please select a row ");
            return;
        }
        BioTechnology c = (BioTechnology) BioTechnologyTble.getValueAt(selectedRow, 1);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        userProcessContainer.add("AddChemicalsJPanel", new AddChemicalsJPanel(userProcessContainer, business, userAccount, BioTechnologyOrganization, enterprise, network, c));
        log.debug(userAccount+" "+"entering add chemicals page");
        layout.next(userProcessContainer);
    }//GEN-LAST:event_addChemicalButtonActionPerformed

