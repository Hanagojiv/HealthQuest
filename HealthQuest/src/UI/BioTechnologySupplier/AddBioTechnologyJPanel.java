    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) BioTechnologyTble.getModel();

        model.setRowCount(0);

        for (BioTechnology d : business.getBioTechnologyList().getBioTechnologyList()) {

            Object[] row = new Object[2];
            row[0] = d.getBioTechnologyId();
            row[1] = d;
            model.addRow(row);

        }

    }

